export function EmojiPicker({ onEmojiSelect, className = "" }: EmojiPickerProps) {
  const [activeCategory, setActiveCategory] = useState('recent');

  return (
    <div className={`
      bg-white rounded-lg shadow-lg border border-gray-200 
      w-80 h-96 flex flex-col overflow-hidden
      ${className}
    `}>
      {/* Category Tabs */}
      <div className="flex border-b border-gray-200 bg-gray-50">
        {Object.entries(EMOJI_CATEGORIES).map(([key, category]) => (
          <button
            key={key}
            onClick={() => setActiveCategory(key)}
            className={`
              flex-1 px-2 py-2 text-xs font-medium transition-colors
              ${activeCategory === key 
                ? 'bg-blue-500 text-white' 
                : 'text-gray-600 hover:bg-gray-100'
              }
            `}
          >
            {category.name}
          </button>
        ))}
      </div>

      {/* Emoji Grid */}
      <div className="flex-1 overflow-y-auto p-2">
        <div className="grid grid-cols-8 gap-1">
          {EMOJI_CATEGORIES[activeCategory as keyof typeof EMOJI_CATEGORIES].emojis.map((emoji) => (
            <button
              key={emoji}
              onClick={() => onEmojiSelect(emoji)}
              className="
                w-8 h-8 flex items-center justify-center text-lg
                rounded hover:bg-gray-100 transition-colors
                focus:outline-none focus:ring-2 focus:ring-blue-500
              "
              title={emoji}
            >
              {emoji}
            </button>
          ))}
        </div>
      </div>
    </div>
  );
}